package 복습2;

public class Ex05 {

	private String name;
	private int age;
	
	public Ex05() {
		
	}
	
	public Ex05(String name, int age) {
		super();
		this.name = name;
		this.age = age;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	
	
}
