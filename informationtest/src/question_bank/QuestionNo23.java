package question_bank;

public class QuestionNo23 {
	public static void main(String[] args) {
		/* Python
		 * a, c = 32, -3
		 * b = a << 2
		 * a >>= 3
		 * c = c << 2
		 * print(a, b, c)
		 */
		int a = 32, b, c = -3;
		b = a << 2;
		a >>= 3;
		c = c << 2;
		System.out.printf("%d %d %d", a, b, c);
		
	}
	
}
